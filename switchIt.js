function switchItUp(number) {
  let out = "";
  switch (number) {
    case 0:
      out = "Zero";
      break;    
    case 1:
      out = "One";
      break;
    case 2:
      out = "Two";
      break;
    case 3:
      out = "Three";
      break;
    case 4:
      out = "Four";
      break;
    case 5:
      out = "Five";
      break;
    case 6:
      out = "Six";
      break;
    case 7:
      out = "Seven";
      break;
    case 8:
      out = "Eight";
      break;
    case 9:
      out = "Nine";
      break;
  }
  return out;
}